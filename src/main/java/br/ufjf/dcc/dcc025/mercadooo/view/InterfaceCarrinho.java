    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        try{
            int qtd = (Integer) jsQuantidade.getValue();
            Produto produto = (Produto) cbProdutos.getSelectedItem();
            Item item = new Item(produto, qtd);
            Dados.pedido.adicionarItem(item);
           

            
            
            
            DefaultTableModel val = new DefaultTableModel(new String [] {
                "Nome", "Quantidade", "Valor"
            }, 0);
            for (Item item1 : Dados.pedido.getItens()) {
                val.addRow(new Object[]{item1.getProduto(), item1.getQuantidade(), item1.getTotal()});
            }
            val.addRow(new Object[]{"Total", null, Dados.pedido.valorTotal()});
            jtPedido.setModel(val);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(rootPane, "Digite um n√∫mero");
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

